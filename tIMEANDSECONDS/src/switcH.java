public class switcH {
    public static void main(String[] args) {
        int switchValue = 5;
        switch (switchValue) {
            case 1:
                System.out.println("value is 1");
                break;
            case 2:
                System.out.println("value is 2");
                break;
            default:
                System.out.println("not equal to 2 or 1");
                break;

        }
    }
}
